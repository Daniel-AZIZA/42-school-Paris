#include "libft.h"
#include <stdlib.h>

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*current;
	t_list	*next;

	current = *lst;
	*lst = NULL;
	while (current)
	{
		next = current->next;
		del(current->content);
		free(current);
		current = next;
	}
}
